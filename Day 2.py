# Escape Sequences:
# Escape sequences allow special characters in strings.
# They must be written inside a string.

print("1. New Line (\\n):")
print("Hello\nWorld")

print("\n2. Tab (\\t):")
print("Hello\tWorld")

print("\n3. Backslash (\\\\):")
print("C:\\Users\\Shujaath")

print("\n4. Single Quote (\\'):")
print('It\'s OK')

print("\n5. Double Quote (\\\"):")
print("He said \"Hi\"")

print("\n6. Carriage Return (\\r):")
print("Hello\rWorld")

print("\n7. Backspace (\\b):")
print("Helloo\b")

print("\n8. Form Feed (\\f):")
print("Hello\fWorld")

print("\n9. Bell Alert (\\a):")
print("Beep sound if system supports\a")

print("\n10. Vertical Tab (\\v):")
print("Hello\vWorld")

# Raw String
print("\n11. Raw String (r'...'):")
print(r"Hello\nWorld")